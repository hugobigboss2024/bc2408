package listofstudent;

public class Stundent3 {
    private String id;
    private String name;
    private int age;
    private String classname;
    public Stundent3() {
    }
    public Stundent3(String id, String name, int age, String classname) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.classname = classname;
    }
    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public int getAge() {
        return age;
    }
    public void setAge(int age) {
        this.age = age;
    }
    public String getClassname() {
        return classname;
    }
    public void setClassname(String classname) {
        this.classname = classname;
    }
    
}
